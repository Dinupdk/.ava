public class Ewhile {
    public static void main(String[] args) {
        System.out.println("While");
        int count = 0;  
        while (count < 5) {  
            System.out.println("Count: " + count);  
            count++;  
        }  
        //* Do While */
        System.out.println("do while");
        int i=1;    
        do{    
            System.out.println(i);    
            i++;    
        }while(i<=10);    
    }
    
}
