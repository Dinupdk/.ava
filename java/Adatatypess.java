public class Adatatypess {
    public static void main(String[] args) {
        boolean a=true;
        boolean b=false;
        System.out.println("Boolean value of a is "+a);
        System.out.println("Boolean value of b is "+b);

        byte c=10;byte d=-20;
        System.out.println("Byte type of c,d is "+c+","+d);

        short e=32767;
        short f=-10500;
        System.out.println("Short type of e,f is "+e+","+f);

        int g=100000;
        int h=-2147483648;
        System.out.println("Int type of g,h is "+g+","+h);

        long i = -92233;
        long j = 2147483647L;
        System.out.println("Long type of i,j is "+i+","+j);

        float k=234.5F;
        float l=-123.45f;
        System.out.println("Float type of k,l is "+k+","+l);

        double m=12.3;
        double n=123.45;
        System.out.println("Double type of m,n is "+m+","+n);

        char o='O';
        char p='P'; // must write in single codes('')
        
        System.out.println("Char datatype of o,p is "+o+","+p);

    }
    
}
